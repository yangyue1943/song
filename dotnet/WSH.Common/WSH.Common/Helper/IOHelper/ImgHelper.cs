        #region 图片转64位字符
        /// <summary>
        /// 保存64位字符串的图片格式为本地文件
        /// </summary>
        /// <param name="base64String"></param>
        /// <param name="saveFileName"></param>
        public static void SaveBase64(string base64String, string saveFileName)
        {
            byte[] arr = Convert.FromBase64String(base64String);
            MemoryStream ms = new MemoryStream(arr);
            Bitmap bmp = new Bitmap(ms);

            ImageFormat fmt = ImageFormat.Jpeg;
            string ext = Path.GetExtension(saveFileName);
            switch (ext.ToLower())
            {
                case ".gif": fmt = ImageFormat.Gif; break;
                case ".bmp": fmt = ImageFormat.Bmp; break;
                case ".ico": fmt = ImageFormat.Icon; break;
                case ".png": fmt = ImageFormat.Png; break;
            }
            bmp.Save(saveFileName, fmt);
            ms.Close();
        }
        /// <summary>
        /// 将图片转成64位字符串
        /// </summary>
        /// <param name="fileName"></param>
        /// <returns></returns>
        public static string ToBase64(string fileName)
        {
            if(!File.Exists(fileName)){
                return string.Empty;
            }
            byte[] arr = File.ReadAllBytes(fileName);
            String strBaser64 = Convert.ToBase64String(arr);
            return strBaser64;
        }
